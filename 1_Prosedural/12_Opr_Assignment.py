# OPERATOR ASSIGNMENT
# Adalah metode operasi yang ditulis secara singkat

# Contoh :

# 1.  Operator Assignment untuk Penjumlahan
print ('\n=====Operator Ass. Penjumlahan=====')

a = 10
print ('Nilai a = ', a)

a += 5   # Artinya a + 5 
print ('Nilai a setelah assignment = ', a)


# 2.  Operator Assignment untuk Pengurangan
print ('\n=====Operator Ass. Pengurangan=====')

a = 10
print ('Nilai a = ', a)

a -= 5   # Artinya a - 5 
print ('Nilai a setelah assignment = ', a)


# 3.  Operator Assignment untuk Perkalian
print ('\n=====Operator Ass. Perkalian=====')

a = 10
print ('Nilai a = ', a)

a *= 5   # Artinya a * 5 
print ('Nilai a setelah assignment = ', a)



# 4.  Operator Assignment untuk Pembagian
print ('\n=====Operator Ass. Pembagian=====')

a = 10
print ('Nilai a = ', a)

a /= 5   # Artinya a / 5 
print ('Nilai a setelah assignment = ', a)


# 5.  Operator Assignment untuk Modulus
print ('\n=====Operator Ass. Pembagian=====')

a = 10
print ('Nilai a = ', a)

a %= 3   # Artinya a / 3 
print ('Nilai a setelah assignment = ', a)


# 6.  Operator Assignment untuk floor devisiasi
print ('\n=====Operator Ass. Floor Devisiasi=====')

a = 10
print ('Nilai a = ', a)

a //= 3   # Artinya a // 3 
print ('Nilai a setelah assignment = ', a)


# 7.  Operator Assignment untuk eksponen
print ('\n=====Operator Ass. eksponen=====')

a = 10
print ('Nilai a = ', a)

a **= 3   # Artinya a ** 3 
print ('Nilai a setelah assignment = ', a)


# 8.  Operator Assignment Logika OR (|)
print ('\n=====Operator Ass. OR (|)=====')

a = True
print ('Nilai a = ', a)

a |= True   # Artinya a or True
print ('Nilai a setelah assignment = ', a)


# 9.  Operator Assignment Logika AND (&)
print ('\n=====Operator Ass. AND (&)=====')

a = True
print ('Nilai a = ', a)

a &= True   # Artinya a and True 
print ('Nilai a setelah assignment = ', a)


# 10.  Operator Assignment Logika XOR (^)
print ('\n=====Operator Ass. XOR (^)=====')

a = True
print ('Nilai a = ', a)

a ^= True   # Artinya a xor True 
print ('Nilai a setelah assignment = ', a)

# note : untuk operator assignment logika, operator ditulis dengan tanda
# OR (|)
# AND (&)
# XOR (^)