# Cara membuat class
class mekanik:          # Cara penulisan class
    name = 'nama'       # Ini adalah template/variable instance

Yoga = mekanik()        
Rizal = mekanik()

Yoga.name = 'Yoga Deliz Pratama'          # Variable Instance setelah dirubah
Rizal.name = 'Ahmad Rizal Mutaqin'

print(Yoga.name)          # Display
print(Rizal.name)