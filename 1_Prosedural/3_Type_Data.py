# Tipe - tipe data pada bahasa Python
# misal : a = 10, a adalah variabel, 10 adalah nilai dari a
# nilai a bisa dikelompokan menjadi beberapa tipe data, yaitu:

print("\n=== TIPE - TIPE DATA ===\n")

# I. Tipe data angka bulat tanpa koma (Integer)
a = 10
print("Data Integer = ", a,"(Angka bulat tanpa koma)")

# II. Tipe data angka dengan koma (Float)
b = 5.5
print("Data Float = ", b,"(Angka dengan koma)")

# III. Tipe data kumpulan karakter/huruf (String)
c = "Eka Wahana Putra" # String harus pake tanda petik
print("Data String = ", c,"(Kumpulan Huruf/Karakter)")

#IV. Tipe data biner true or false (Boolean)
d = False
print("Data Boolean = ", d,"(Data biner true or false)")

