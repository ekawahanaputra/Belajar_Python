# Tkinter adalah module / package built in pada Python ynag berfungsi untuk membuat GUI (Graphical User Interface) berbasis python
# Adapun cara membuatnya adalah sebagai berikut:

# 1. langkah pertama adalah mengimport module / package Tkinter dengan syntax berikut:
from tkinter import *


# 2. Deklarasikan sebuah variable untuk membuka GUI
root = Tk()              # root adalah nama variable. (penamaan bisa bebas terserah anda)
                         # Tk() adalah sebuah class pada module tkinter untuk menampilkan GUI

# 3. Membuat tampilan GUI agar melakukan looping terus menerus dengan syntax dibawah
root.mainloop()          # root adalah nama variable untuk mendeklarasi, dan mainloop() adalah sebuah method dari module tkinter



